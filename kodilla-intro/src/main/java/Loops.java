public class Loops {
    public static void main(String[] args) {

        int[] numbers = new int[]{5, 20, 52};

        int sum = 0;





        for (int i = 0; i < numbers.length; i++) {
         int num = numbers[i];

            sum = sum + num;


        }

        System.out.println(sum);


        }

    }
